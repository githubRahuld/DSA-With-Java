public class Basics {
    public static void main(String[] args) {
        // bitwise &
        System.out.println(5 & 6); // 4

        // Bitwise |
        System.out.println(5 | 6);

        // Bitwise XOR
        //diffrent bit gives 1
        System.out.println(5 ^ 6); 

        // Bitwise One's complement
        System.out.println(~5); //-6

        // Left shift <<
        System.out.println(5<<2);

        //right shift >>
        System.out.println(5>>2);
    }    
}
